	private String srcEntiryName;     // 原单据类型名称
	private String srcObjectNumber;		   // 源单据单据编码
	
	private String mappingName;				// 转换规则 名称
	private String mappingNumber;			// 转换规则编码
	private String destEntityBostype;  // 目标单据bostype
	private String destEntityName;     // 目标单据名称
	private String destObjectNumber;   // 目标单据编码
